"""
Сервис предпочтений по эмодзи в ответах Панды.

Одна ответственность: парсинг фраз пользователя и формирование фрагмента
системного промпта. Не нарушает структуру ответов — эмодзи только украшение.
"""

from typing import Literal

# Фразы «не использовать эмодзи» (разные формулировки)
NO_EMOJI_PHRASES = (
    "не ставь эмодзи",
    "не ставь смайлики",
    "без эмодзи",
    "без смайликов",
    "не используй эмодзи",
    "не используй смайлики",
    "не надо эмодзи",
    "не нужны эмодзи",
    "не пиши эмодзи",
    "без эмоджи",
    "не эмодзи",
)

# Фразы «добавлять эмодзи»
YES_EMOJI_PHRASES = (
    "добавляй эмодзи",
    "ставь эмодзи",
    "с эмодзи",
    "можно эмодзи",
    "хочу эмодзи",
    "со смайликами",
    "добавляй смайлики",
    "с эмоджи",
    "используй эмодзи",
)


def parse_emoji_preference_from_message(message: str) -> Literal[False, True] | None:
    """
    Извлекает предпочтение по эмодзи из текста сообщения пользователя.

    Returns:
        False — пользователь просит не использовать эмодзи.
        True — пользователь просит использовать эмодзи.
        None — в сообщении нет явной просьбы (предпочтение не менять).
    """
    if not message or not isinstance(message, str):
        return None
    lower = message.lower().strip()
    for phrase in NO_EMOJI_PHRASES:
        if phrase in lower:
            return False
    for phrase in YES_EMOJI_PHRASES:
        if phrase in lower:
            return True
    return None


def compute_allow_emoji_this_turn(chat_history: list[dict]) -> bool:  # noqa: ARG001
    """Эмодзи разрешены в каждом ответе (если пользователь не отключил)."""
    return True


def get_emoji_prompt_snippet(
    emoji_in_chat: bool | None,
    allow_emoji_this_turn: bool,  # noqa: ARG001
) -> str | None:
    """
    Возвращает фрагмент системного промпта про эмодзи.

    Args:
        emoji_in_chat: None = по умолчанию (эмодзи включены), False = отключены, True = включены.
        allow_emoji_this_turn: не используется (оставлен для обратной совместимости).

    Returns:
        Строка для добавления в системный промпт.
    """
    if emoji_in_chat is False:
        return (
            "Эмодзи в ответах не используй. "
            "Структура ответа (абзацы, списки, выделения, грамматика) соблюдается строго."
        )
    # emoji_in_chat is True или None — обязательно 1–2 тематических эмодзи
    return (
        "Обязательно добавляй 1–2 тематических эмодзи по теме ответа (достаточно в каждом втором ответе). "
        "Ставь в начале ключевого абзаца или рядом с основным термином. "
        "Примеры по предметам: 🧮 математика, 📐 геометрия, 📖 литература, ✏️ русский язык, "
        "🌿 биология, 🔬 физика, ⚗️ химия, 🌍 география, 🎭 история, 💡 идея, ✅ верно, "
        "📊 график/таблица, 🗺️ карта. Не в каждой строке; структура и грамматика важнее. "
        "Если в ответе уже есть эмодзи — не добавляй дополнительные."
    )
