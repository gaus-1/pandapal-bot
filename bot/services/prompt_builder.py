    def _get_greeting_context(
        self,
        chat_history: list[dict] | None,
        is_history_cleared: bool,
        user_message: str,
        is_auto_greeting_sent: bool,
        user_name: str | None = None,
        user_grade: int | None = None,
    ) -> str | None:
        """–û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω—É–∂–Ω–æ –ª–∏ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–æ–≤–∞—Ç—å—Å—è –∏–ª–∏ –ø—Ä–æ—â–∞—Ç—å—Å—è."""
        if not chat_history:
            chat_history = []

        user_message_lower = user_message.lower().strip()

        # –ü—Ä–æ–≤–µ—Ä–∫–∞, –∑–¥–æ—Ä–æ–≤–∞–ª–∞—Å—å –ª–∏ –ø–∞–Ω–¥–∞
        ai_greeted = False
        if is_auto_greeting_sent:
            ai_greeted = True
        else:
            for msg in chat_history:
                if msg.get("role") == "assistant":
                    txt = msg.get("text", "").lower()
                    if (
                        "–ø—Ä–∏–≤–µ—Ç" in txt
                        or "—á–µ–º –º–æ–≥—É –ø–æ–º–æ—á—å" in txt
                        or "–Ω–∞—á–Ω–µ–º" in txt
                        or "–ø–∞–Ω–¥–∞–ø–∞–ª" in txt
                    ):
                        ai_greeted = True
                        break

