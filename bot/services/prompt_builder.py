"""
–ú–æ–¥—É–ª—å –¥–ª—è —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏—è —Å–∏—Å—Ç–µ–º–Ω—ã—Ö –ø—Ä–æ–º–ø—Ç–æ–≤ –¥–ª—è YandexGPT.

–î–æ–±–∞–≤–ª—è–µ—Ç –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–∏–π –∫–æ–Ω—Ç–µ–∫—Å—Ç (–∏–º—è, —Å—Ç–∞—Ç—É—Å –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è, —Ä–µ–¥–∏—Ä–µ–∫—Ç –Ω–∞ —É—á–µ–±—É)
–∫ –±–∞–∑–æ–≤–æ–º—É —Å–∏—Å—Ç–µ–º–Ω–æ–º—É –ø—Ä–æ–º–ø—Ç—É.
"""

import random

from bot.config.prompts import AI_SYSTEM_PROMPT


class PromptBuilder:
    """–ü–æ—Å—Ç—Ä–æ–∏—Ç–µ–ª—å –ø—Ä–æ–º–ø—Ç–æ–≤ –¥–ª—è YandexGPT."""

    def __init__(self):
        self.base_prompt = AI_SYSTEM_PROMPT

    def build_system_prompt(
        self,
        user_message: str = "",
        user_name: str | None = None,
        chat_history: list[dict] | None = None,
        is_history_cleared: bool = False,
        message_count_since_name: int = 0,
        non_educational_questions_count: int = 0,
        user_age: int | None = None,
        is_auto_greeting_sent: bool = False,  # –î–æ–±–∞–≤–ª–µ–Ω–æ –¥–ª—è –ª–æ–≥–∏–∫–∏ 5 —Å–µ–∫—É–Ω–¥
    ) -> str:
        """
        –ü–æ—Å—Ç—Ä–æ–∏—Ç—å —Å–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç —Å —É—á–µ—Ç–æ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞.
        """
        # 1. –ë–∞–∑–æ–≤—ã–π –ø—Ä–æ–º–ø—Ç (–õ–∏—á–Ω–æ—Å—Ç—å, –∑–∞–ø—Ä–µ—â–µ–Ω–Ω–æ–µ, –±–∞–∑–æ–≤–∞—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∞)
        prompt = self.base_prompt

        # 2. –ê–¥–∞–ø—Ç–∞—Ü–∏—è –ø–æ–¥ –≤–æ–∑—Ä–∞—Å—Ç (–≤—Å—Ç–∞–≤–ª—è–µ–º —Ä–∞–Ω—å—à–µ, —á—Ç–æ–±—ã –º–æ–¥–µ–ª—å –≤—ã–±–∏—Ä–∞–ª–∞ —Ç–æ–Ω)
        if user_age:
            prompt += f"\n\nüìö –í–æ–∑—Ä–∞—Å—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: {user_age} –ª–µ—Ç. –ê–¥–∞–ø—Ç–∏—Ä—É–π –æ–±—ä—è—Å–Ω–µ–Ω–∏—è."

        # 3. –ê–Ω–∞–ª–∏–∑ —Å–æ—Å—Ç–æ—è–Ω–∏—è —á–∞—Ç–∞ (–ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è, –ø—Ä–æ—â–∞–Ω–∏—è)
        greeting_instructions = self._get_greeting_context(
            chat_history=chat_history,
            is_history_cleared=is_history_cleared,
            user_message=user_message,
            is_auto_greeting_sent=is_auto_greeting_sent,
        )
        if greeting_instructions:
            prompt += f"\n\nüëã {greeting_instructions}"

        # 4. –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –∏–º–µ–Ω–∏ (–µ—Å–ª–∏ –µ—Å—Ç—å –∏ –ø—Ä–æ—à–ª–æ —Å–æ–æ–±—â–µ–Ω–∏–π)
        if user_name and message_count_since_name >= 5:
            # –°–ª—É—á–∞–π–Ω–æ–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –∏–º–µ–Ω–∏, —á—Ç–æ–±—ã –Ω–µ –±—ã—Ç—å –Ω–∞–∑–æ–π–ª–∏–≤—ã–º
            if 5 <= message_count_since_name <= 10:
                if random.random() < 0.4:
                    prompt += f"\n\nüë§ –ò–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: {user_name}. –û–±—Ä–∞—â–∞–π—Å—è –∫ –Ω–µ–º—É –ø–æ –∏–º–µ–Ω–∏ –≤ –Ω–∞—á–∞–ª–µ –æ—Ç–≤–µ—Ç–∞, –Ω–æ —Ç–æ–ª—å–∫–æ –≤ —ç—Ç–æ–º —Å–æ–æ–±—â–µ–Ω–∏–∏. –ë—É–¥—å –¥—Ä—É–∂–µ–ª—é–±–Ω–æ–π."
            elif message_count_since_name > 20:
                # –°–Ω–æ–≤–∞ –Ω–∞–ø–æ–º–∏–Ω–∞–µ–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∏–º—è, –µ—Å–ª–∏ –ø—Ä–æ—à–ª–æ –º–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏–π
                prompt += (
                    f"\n\nüë§ –ò–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: {user_name}. –ü–æ—Ä–∞ —Å–Ω–æ–≤–∞ –æ–±—Ä–∞—Ç–∏—Ç—å—Å—è –∫ –Ω–µ–º—É –ø–æ –∏–º–µ–Ω–∏."
                )

        # 5. –†–µ–¥–∏—Ä–µ–∫—Ç –Ω–∞ —É—á–µ–±—É (–µ—Å–ª–∏ –º–Ω–æ–≥–æ –±–æ–ª—Ç–æ–≤–Ω–∏)
        if non_educational_questions_count >= 3:
            prompt += (
                "\n\nüö´ –í–ê–ñ–ù–û: –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ –¥–∞–≤–Ω–æ –Ω–µ –∑–∞–¥–∞–µ—Ç —É—á–µ–±–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤. "
                "–í–µ–∂–ª–∏–≤–æ, –Ω–æ —Ç–≤–µ—Ä–¥–æ –ø—Ä–µ–¥–ª–æ–∂–∏ –≤–µ—Ä–Ω—É—Ç—å—Å—è –∫ —à–∫–æ–ª—å–Ω—ã–º –ø—Ä–µ–¥–º–µ—Ç–∞–º. "
                "–ù–∞–ø—Ä–∏–º–µ—Ä: '–ò–Ω—Ç–µ—Ä–µ—Å–Ω–æ! –ê –∫–∞–∫ —É —Ç–µ–±—è –¥–µ–ª–∞ —Å –º–∞—Ç–µ–º–∞—Ç–∏–∫–æ–π? –ï—Å—Ç—å –∑–∞–¥–∞—á–∏, –≤ –∫–æ—Ç–æ—Ä—ã—Ö —è –º–æ–≥—É –ø–æ–º–æ—á—å?'"
            )

        # 6. –§–ò–ù–ê–õ–¨–ù–û–ï –£–¢–û–ß–ù–ï–ù–ò–ï (–°–∞–º–æ–µ –≤–∞–∂–Ω–æ–µ - –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –≤ –∫–æ–Ω—Ü–µ –ø—Ä–æ–º–ø—Ç–∞!)
        # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —è–≤–Ω–æ –∑–∞–¥–∞–µ—Ç –≤–æ–ø—Ä–æ—Å –ø–æ —É—á–µ–±–µ, –º—ã –µ—â–µ —Ä–∞–∑ –ø–æ–¥—á–µ—Ä–∫–∏–≤–∞–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É,
        # —á—Ç–æ–±—ã —ç—Ç–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ –ø–µ—Ä–µ–±–∏–ª–æ –ª—é–±—ã–µ –¥—Ä—É–≥–∏–µ —Å–∏—Å—Ç–µ–º–Ω—ã–µ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏.
        if self._is_educational_question(user_message):
            prompt += self._get_strict_formatting_instruction()

        return prompt

    def _is_educational_question(self, text: str) -> bool:
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –ø–æ—Ö–æ–∂–µ –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞ —É—á–µ–±–Ω—ã–π –≤–æ–ø—Ä–æ—Å."""
        text_lower = text.lower()
        # –ü—Ä–æ—Å—Ç—ã–µ –º–∞—Ä–∫–µ—Ä—ã (–º–æ–∂–Ω–æ —Ä–∞—Å—à–∏—Ä–∏—Ç—å)
        educational_keywords = [
            "–ø–æ–º–æ–≥–∏",
            "—Ä–µ—à–∏",
            "–æ–±—ä—è—Å–Ω–∏",
            "–∑–∞–¥–∞—á–∞",
            "–ø—Ä–∏–º–µ—Ä",
            "—É—Ä–∞–≤–Ω–µ–Ω–∏–µ",
            "–∫—Ç–æ",
            "–∫–∞–∫",
            "–ø–æ—á–µ–º—É",
            "–∑–∞—á–µ–º",
            "—á–µ–º",
            "–æ—Ç–ª–∏—á–∞–µ—Ç—Å—è",
            "–ø–µ—Ä–µ–≤–µ–¥–∏",
            "–Ω–∞–ø–∏—à–∏",
        ]
        # –ï—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –¥–ª–∏–Ω–Ω–æ–µ –∏–ª–∏ —Å–æ–¥–µ—Ä–∂–∏—Ç –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ - —Å—á–∏—Ç–∞–µ–º —ç—Ç–æ —É—á–µ–±–æ–π
        return len(text) > 5 and any(kw in text_lower for kw in educational_keywords)

    def _get_strict_formatting_instruction(self) -> str:
        """–ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è, –∫–æ—Ç–æ—Ä–∞—è –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç —Ñ–æ—Ä–º–∞—Ç '—Å—Ç–æ–ª–±–∏–∫'. –í—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø–æ—Å–ª–µ–¥–Ω–µ–π."""
        return """
‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
üìã –í–ù–ò–ú–ê–ù–ò–ï: –°–¢–†–£–ö–¢–£–†–ê –û–¢–í–ï–¢–ê (–°–õ–ï–î–£–ô –≠–¢–û–ú–£ –ü–†–ê–í–ò–õ–£)
‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–∞–¥–∞–ª —É—á–µ–±–Ω—ã–π –≤–æ–ø—Ä–æ—Å. –û–±—è–∑–∞—Ç–µ–ª—å–Ω–æ —Ä–∞–∑–±–µ—Ä–∏ –æ—Ç–≤–µ—Ç –Ω–∞ –ª–æ–≥–∏—á–µ—Å–∫–∏–µ –±–ª–æ–∫–∏.

1. –í—Å—Ç—É–ø–ª–µ–Ω–∏–µ (–∫—Ä–∞—Ç–∫–æ).
2. –†–µ—à–µ–Ω–∏–µ/–û–±—ä—è—Å–Ω–µ–Ω–∏–µ (–≤ —Å—Ç–æ–ª–±–∏–∫, –∫–∞–∂–¥—ã–π —à–∞–≥ —Å –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏).
3. –û—Ç–≤–µ—Ç (—Ä–µ–∑—É–ª—å—Ç–∞—Ç).
4. –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ–Ω–∏–º–∞–Ω–∏—è ("–ü–æ–Ω—è—Ç–Ω–æ? –ò–ª–∏ —Ä–∞—Å—Å–∫–∞–∂–∞—Ç—å –ø–æ–¥—Ä–æ–±–Ω–µ–µ?").

–ù–µ –ø–∏—à–∏ –≤—Å—ë –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É. –ò—Å–ø–æ–ª—å–∑—É–π –ø–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫ –º–µ–∂–¥—É –¥–µ–π—Å—Ç–≤–∏—è–º–∏!
"""

    def _get_greeting_context(
        self,
        chat_history: list[dict] | None,
        is_history_cleared: bool,
        user_message: str,
        is_auto_greeting_sent: bool,
    ) -> str | None:
        """–û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω—É–∂–Ω–æ –ª–∏ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–æ–≤–∞—Ç—å—Å—è –∏–ª–∏ –ø—Ä–æ—â–∞—Ç—å—Å—è."""
        if not chat_history:
            chat_history = []

        user_message_lower = user_message.lower().strip()

        # –ü—Ä–æ–≤–µ—Ä–∫–∞, –∑–¥–æ—Ä–æ–≤–∞–ª–∞—Å—å –ª–∏ –ø–∞–Ω–¥–∞ (—É—á–∏—Ç—ã–≤–∞–µ–º –∞–≤—Ç–æ-–ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ –æ—Ç —Å–∏—Å—Ç–µ–º—ã)
        ai_greeted = False
        if is_auto_greeting_sent:
            ai_greeted = True
        else:
            for msg in chat_history:
                if msg.get("role") == "assistant":
                    txt = msg.get("text", "").lower()
                    if "–ø—Ä–∏–≤–µ—Ç" in txt or "—á–µ–º –º–æ–≥—É –ø–æ–º–æ—á—å" in txt or "–∑–¥—Ä–∞–≤—Å—Ç–≤—É–π" in txt:
                        ai_greeted = True
                        break

        # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø—Ä–æ—â–∞–µ—Ç—Å—è
        farewell_keywords = ["–ø–æ–∫–∞", "–¥–æ —Å–≤–∏–¥–∞–Ω–∏—è", "–¥–æ —Å–≤–∏–¥–∞–Ω—å—è", "–ø—Ä–æ—â–∞–π", "bye"]
        if any(fw in user_message_lower for fw in farewell_keywords):
            return "–ü–æ–ø—Ä–æ—â–∞–π—Å—è —Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º. –°–∫–∞–∂–∏ –∫–æ—Ä–æ—Ç–∫–æ –∏ –≤–µ–∂–ª–∏–≤–æ. –ù–µ –ø—Ä–æ—â–∞–π—Å—è, –µ—Å–ª–∏ –æ–Ω –ø—Ä–æ—Å—Ç–æ –∑–∞–¥–∞–ª –≤–æ–ø—Ä–æ—Å."

        # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–¥–æ—Ä–æ–≤–∞–µ—Ç—Å—è –∏–ª–∏ —ç—Ç–æ –Ω–∞—á–∞–ª–æ –¥–∏–∞–ª–æ–≥–∞
        greeting_keywords = ["–ø—Ä–∏–≤–µ—Ç", "–∑–¥—Ä–∞–≤—Å—Ç–≤—É–π", "–¥–æ–±—Ä—ã–π", "–∑–¥—Ä–∞–≤–æ–≤–∞", "—Ö–∞–π", "hello", "hi"]
        user_greeted = any(gw in user_message_lower for gw in greeting_keywords)

        if (not chat_history or is_history_cleared) and user_greeted:
            return "–ü–æ–ø—Ä–∏–≤–µ—Ç—Å—Ç–≤—É–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è. –°–∫–∞–∂–∏ '–ü—Ä–∏–≤–µ—Ç! –Ø PandaPal, –≥–æ—Ç–æ–≤ –ø–æ–º–æ—á—å —Ç–µ–±–µ —Å —É—á–µ–±–æ–π! –ß–µ–º –º–æ–∂–µ–º –∑–∞–Ω—è—Ç—å—Å—è?' –∏–ª–∏ –ø–æ—Ö–æ–∂–µ. –ë—É–¥—å –¥—Ä—É–∂–µ–ª—é–±–Ω–æ–π!"

        if not ai_greeted and not user_greeted and len(chat_history) == 0:
            return "–ù–∞—á–Ω–∏ –¥–∏–∞–ª–æ–≥ –±–µ–∑ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è, –Ω–æ –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–ø–∏—Å–∞–ª —á—Ç–æ-—Ç–æ –≤—Ä–æ–¥–µ '–ù–∞—á–Ω–µ–º', –ø–æ–∑–¥–æ—Ä–æ–≤–∞–π—Å—è. –ï—Å–ª–∏ —Å—Ä–∞–∑—É –≤–æ–ø—Ä–æ—Å ‚Äî –æ—Ç–≤–µ—á–∞–π –Ω–∞ –≤–æ–ø—Ä–æ—Å."

        # –ü–∞–Ω–¥–∞ —É–∂–µ –∑–¥–æ—Ä–æ–≤–∞–ª–∞—Å—å
        if ai_greeted:
            return "–í–ù–ò–ú–ê–ù–ò–ï: –¢—ã —É–∂–µ –ø–æ–∑–¥–æ—Ä–æ–≤–∞–ª–∞—Å—å –≤ —ç—Ç–æ–º –¥–∏–∞–ª–æ–≥–µ. –ù–ï –ø–∏—à–∏ '–ü—Ä–∏–≤–µ—Ç', '–ü—Ä–∏–≤–µ—Ç!' –∏–ª–∏ '–†–∞–¥ –ø–æ–º–æ—á—å' —Å–Ω–æ–≤–∞. –ù–∞—á–∏–Ω–∞–π –æ—Ç–≤–µ—Ç —Å—Ä–∞–∑—É —Å —Ä–µ—à–µ–Ω–∏—è –≤–æ–ø—Ä–æ—Å–∞. –ë—É–¥—å –∫—Ä–∞—Ç–∫–æ–π –∏ –¥–µ–ª–æ–≤–æ–π."

        return None


# –ì–ª–æ–±–∞–ª—å–Ω—ã–π —ç–∫–∑–µ–º–ø–ª—è—Ä
_prompt_builder = None


def get_prompt_builder() -> PromptBuilder:
    """–ü–æ–ª—É—á–∏—Ç—å –≥–ª–æ–±–∞–ª—å–Ω—ã–π —ç–∫–∑–µ–º–ø–ª—è—Ä PromptBuilder."""
    global _prompt_builder
    if _prompt_builder is None:
        _prompt_builder = PromptBuilder()
    return _prompt_builder
