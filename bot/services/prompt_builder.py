    def _get_greeting_context(
        self,
        chat_history: list[dict] | None,
        is_history_cleared: bool,
        user_message: str,
        is_auto_greeting_sent: bool,
        user_name: str | None = None,
    ) -> str | None:
        """–û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω—É–∂–Ω–æ –ª–∏ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–æ–≤–∞—Ç—å—Å—è –∏–ª–∏ –ø—Ä–æ—â–∞—Ç—å—Å—è."""
