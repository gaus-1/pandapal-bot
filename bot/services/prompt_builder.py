        farewell_keywords = [
            "–ø–æ–∫–∞",
            "–¥–æ —Å–≤–∏–¥–∞–Ω–∏—è",
            "–¥–æ —Å–≤–∏–¥–∞–Ω—å—è",
            "–ø—Ä–æ—â–∞–π",
            "–ø—Ä–æ—â–∞–π—Ç–µ",
            "bye",
            "goodbye",
            "see you",
            "—É–≤–∏–¥–∏–º—Å—è",
        ]
