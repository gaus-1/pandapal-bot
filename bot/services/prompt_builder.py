        has_question_words = any(
            qw in user_message_lower for qw in ["—á—Ç–æ", "–∫–∞–∫", "–≥–¥–µ", "–∫–æ–≥–¥–∞", "–ø–æ—á–µ–º—É", "–∫—Ç–æ", "?"]
        )
