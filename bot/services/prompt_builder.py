        # –ö–†–ò–¢–ò–ß–ï–°–ö–ò –í–ê–ñ–ù–û: –ü–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏ –∏—Å—Ç–æ—Ä–∏–∏ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –¢–û–õ–¨–ö–û –û–î–ò–ù –†–ê–ó
        # –ï—Å–ª–∏ –∏—Å—Ç–æ—Ä–∏—è –æ—á–∏—â–µ–Ω–∞ –ò –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ —É–∂–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ - –ù–ï –∑–¥–æ—Ä–æ–≤–∞—Ç—å—Å—è —Å–Ω–æ–≤–∞
        if is_history_cleared and ai_greeted:
            return (
                "–í–ù–ò–ú–ê–ù–ò–ï: –ò—Å—Ç–æ—Ä–∏—è –±—ã–ª–∞ –æ—á–∏—â–µ–Ω–∞, –∏ —Ç—ã —É–∂–µ –ø–æ–∑–¥–æ—Ä–æ–≤–∞–ª–∞—Å—å (—á–µ—Ä–µ–∑ /start –∏–ª–∏ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏). "
                "–ù–ï –ø–∏—à–∏ '–ü—Ä–∏–≤–µ—Ç', '–†–∞–¥ –ø–æ–º–æ—á—å', '–ö–æ–Ω–µ—á–Ω–æ', '–ó–¥—Ä–∞–≤—Å—Ç–≤—É–π' –≤ –Ω–∞—á–∞–ª–µ –æ—Ç–≤–µ—Ç–∞. "
                "–°—Ä–∞–∑—É –Ω–∞—á–∏–Ω–∞–π —Å —Ä–µ—à–µ–Ω–∏—è –∏–ª–∏ –æ–±—ä—è—Å–Ω–µ–Ω–∏—è. "
                # –£–±—Ä–∞–Ω–æ —Ç—Ä–µ–±–æ–≤–∞–Ω–∏–µ —Å—Ç—Ä—É–∫—Ç—É—Ä—ã - –¥–∞–µ–º –º–æ–¥–µ–ª–∏ —Å–≤–æ–±–æ–¥—É
            )

        # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–¥–æ—Ä–æ–≤–∞–µ—Ç—Å—è - –¢–û–õ–¨–ö–û –µ—Å–ª–∏ —ç—Ç–æ —è–≤–Ω–æ–µ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ
        greeting_keywords = [
            "–ø—Ä–∏–≤–µ—Ç",
            "–∑–¥—Ä–∞–≤—Å—Ç–≤—É–π",
            "–∑–¥—Ä–∞–≤—Å—Ç–≤—É–π—Ç–µ",
            "–¥–æ–±—Ä—ã–π –¥–µ–Ω—å",
            "–¥–æ–±—Ä–æ–µ —É—Ç—Ä–æ",
            "–¥–æ–±—Ä—ã–π –≤–µ—á–µ—Ä",
            "–∑–¥—Ä–∞–≤–æ–≤–∞",
            "—Ö–∞–π",
            "hi",
            "hello",
        ]
        user_greeted = any(gw in user_message_lower for gw in greeting_keywords)

        # –û–°–û–ë–´–ô –°–õ–£–ß–ê–ô: –ü–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏ –∫–æ—Ä–∑–∏–Ω—ã, –µ—Å–ª–∏ –ø–∞–Ω–¥–∞ —É–∂–µ –ø–æ–∑–¥–æ—Ä–æ–≤–∞–ª–∞—Å—å –∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Ç–æ–∂–µ –ø–æ–∑–¥–æ—Ä–æ–≤–∞–ª—Å—è
