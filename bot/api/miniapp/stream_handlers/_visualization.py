def get_diagram_explanation(visualization_type: str) -> str | None:
    """–ü–æ–ª—É—á–∏—Ç—å –ø–æ–¥—Ä–æ–±–Ω–æ–µ –ø–æ—è—Å–Ω–µ–Ω–∏–µ –¥–ª—è —Ç–∏–ø–∞ –≤–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏–∏."""
    base = DIAGRAM_EXPLANATION_BASES.get(visualization_type)
    if not base:
        return None
    jokes = VISUALIZATION_JOKES.get(visualization_type, [""])
    return f"{base} {random.choice(jokes)}"


